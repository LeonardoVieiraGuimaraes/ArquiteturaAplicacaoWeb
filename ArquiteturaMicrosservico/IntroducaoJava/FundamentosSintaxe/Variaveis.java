package IntroducaoJava.FundamentosSintaxe;

public class Variaveis {

    public static void main(String[] args) {
        // Declaração e inicialização de variáveis de diferentes tipos de dados
        int numeroInteiro = 10;
        double numeroDecimal = 20.5;
        char caractere = 'A';
        boolean verdadeiroOuFalso = true;
        String texto = "Olá, mundo!";

        // Exibindo os valores das variáveis
        System.out.println("Número inteiro: " + numeroInteiro);
        System.out.println("Número decimal: " + numeroDecimal);
        System.out.println("Caractere: " + caractere);
        System.out.println("Booleano: " + verdadeiroOuFalso);
        System.out.println("Texto: " + texto);
    }
}
